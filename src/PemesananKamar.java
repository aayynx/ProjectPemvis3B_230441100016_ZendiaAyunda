    private void loadDataRiwayat(String namaPembeli) {
    StringBuilder dataText = new StringBuilder();

    if (namaPembeli.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Nama pembeli tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_penginapan", "root", "");
        String sql = "SELECT * FROM tb_pesankamar WHERE nama_pembeli = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, namaPembeli);

        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            String tipeKamar = rs.getString("tipe_kamar");
            String nama = rs.getString("nama_pembeli");
            String nomorHp = rs.getString("nomor_hp");
            String jumlahOrang = rs.getString("jumlah_orang");
            String checkin = rs.getString("tgl_checkin");
            String checkout = rs.getString("tgl_checkout");
            String membership = rs.getString("membership");
            String total = rs.getString("total");

            dataText.append("\n-----------------------------\n")
                    .append("Tipe Kamar: ").append(tipeKamar)
                    .append("\nNama: ").append(nama)
                    .append("\nNomor Hp: ").append(nomorHp)
                    .append("\nJumlah Orang: ").append(jumlahOrang)
                    .append("\nTgl Check-in: ").append(checkin)
                    .append("\nTgl Check-out: ").append(checkout)
                    .append("\nMembership: ").append(membership)
                    .append("\nTotal: ").append(total)
                    .append("\n-----------------------------\n");
        }

        rs.close();
        ps.close();
        conn.close();
        
    } catch (SQLException e) {
        e.printStackTrace();
    }

    jTextArea1.setText(dataText.toString()); 
    
    }

    
