    private void TotalPembeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TotalPembeliActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        try {
            Date checkinDate = dateFormat.parse(TglCheckin.getText());
            Date checkoutDate = dateFormat.parse(TglCheckout.getText());
            
            // jumlah hari menginap
            long diff = checkoutDate.getTime() - checkinDate.getTime();
            long hari = diff / (1000 * 60 * 60 * 24);

            if (hari <= 0) {
                JOptionPane.showMessageDialog(this, "Tanggal check-out harus setelah check-in!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // biaya per-malam
            int totalPermalam = 0;
            if (tipekamar1.isSelected()) totalPermalam += 500000;
            if (tipekamar2.isSelected()) totalPermalam += 300000;
            if (tipekamar3.isSelected()) totalPermalam += 250000;

            int jumlahOrang;
            try {
                jumlahOrang = Integer.parseInt(JumlahOrang.getText());
                if (jumlahOrang <= 0) {
                    JOptionPane.showMessageDialog(this, "Jumlah orang harus lebih dari 0!", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Jumlah orang harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            long total = totalPermalam * hari * jumlahOrang;

            if (Mempunyai.isSelected()) {
                total *= 0.9; // Diskon 10%
                JOptionPane.showMessageDialog(this, "Selamat, Anda mendapatkan diskon sebesar 10%!");
            }

            NumberFormat formatter = NumberFormat.getInstance(new Locale("id", "ID"));
            String formattedTotal = formatter.format(total);
            
            TotalPemesanan.setText("Rp " + formattedTotal);

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Format tanggal salah! Gunakan format dd-MM-yyyy", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_TotalPembeliActionPerformed

